

class Produccion(object):
    
    def __init__(self, numero=0, variable='', alfa=''):
        self.numero= numero
        self.variable= variable
        self.alfa= alfa
    
    def obtener_numero(self):
        return self.numero

    def obtener_variable(self):
        return self.variable

    def obtener_alfa(self):
        return self.alfa


    